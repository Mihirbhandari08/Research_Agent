class ResearchRequest(BaseModel):
    """The top-level input to the research agent."""

    run_id: str = Field(default_factory=lambda: str(uuid.uuid4()))
    query: str = Field(..., min_length=10, max_length=2000)
    depth: ResearchDepth = ResearchDepth.STANDARD
    max_sources: int = Field(default=10, ge=1, le=50)
    focus_domains: list[str] = Field(
        default_factory=list,
        description="Optional domains to focus search on (e.g. arxiv.org)",
    )
    output_format: OutputFormat = OutputFormat.MARKDOWN
    created_at: datetime = Field(default_factory=datetime.utcnow)
    metadata: dict[str, Any] = Field(default_factory=dict)

    @property
    def depth_config(self) -> dict[str, int]:
        """Return depth-specific limits."""
        configs = {
            ResearchDepth.QUICK:    {"max_tasks": 3, "max_critic_passes": 1, "max_sources": min(self.max_sources, 5)},
            ResearchDepth.STANDARD: {"max_tasks": 6, "max_critic_passes": 2, "max_sources": min(self.max_sources, 12)},
            ResearchDepth.DEEP:     {"max_tasks": 12, "max_critic_passes": 3, "max_sources": min(self.max_sources, 25)},
        }
        return configs[self.depth]
