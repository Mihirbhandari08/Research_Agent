class Critique(BaseModel):
    """The Critic's evaluation of the current research state."""

    critique_id: str = Field(default_factory=lambda: str(uuid.uuid4()))
    run_id: str
    pass_number: int = 1
    gaps: list[ResearchGap] = Field(default_factory=list)
    contradictions: list[Contradiction] = Field(default_factory=list)
    weak_sources: list[str] = Field(
        default_factory=list,
        description="source_ids flagged as low credibility",
    )
    overall_confidence: float = Field(default=0.0, ge=0.0, le=1.0)
    sufficient: bool = False
    reasoning: str = ""
    suggested_follow_up_queries: list[str] = Field(default_factory=list)
    created_at: datetime = Field(default_factory=datetime.utcnow)


