class ReportSection(BaseModel):
    """A single section of the final report."""

    title: str
    content: str
    supporting_finding_ids: list[str] = Field(default_factory=list)


class Citation(BaseModel):
    """A formatted citation in the final report."""

    citation_id: str = Field(default_factory=lambda: str(uuid.uuid4()))
    source: Source
    referenced_in: list[str] = Field(
        default_factory=list,
        description="section titles that reference this citation",
    )


class TokenUsage(BaseModel):
    """Tracks LLM token consumption for cost accounting."""

    prompt_tokens: int = 0
    completion_tokens: int = 0
    total_tokens: int = 0
    estimated_cost_usd: float = 0.0


class FinalReport(BaseModel):
    """The Synthesizer's final structured research report."""

    report_id: str = Field(default_factory=lambda: str(uuid.uuid4()))
    run_id: str
    query: str
    executive_summary: str
    sections: list[ReportSection] = Field(default_factory=list)
    citations: list[Citation] = Field(default_factory=list)
    overall_confidence: float = Field(default=0.0, ge=0.0, le=1.0)
    confidence_level: ConfidenceLevel = ConfidenceLevel.MEDIUM
    gaps_acknowledged: list[ResearchGap] = Field(default_factory=list)
    contradictions_noted: list[Contradiction] = Field(default_factory=list)
    total_sources: int = 0
    total_findings: int = 0
    critic_passes: int = 0
    token_usage: TokenUsage = Field(default_factory=TokenUsage)
    output_format: OutputFormat = OutputFormat.MARKDOWN
    created_at: datetime = Field(default_factory=datetime.utcnow)
    elapsed_seconds: float = 0.0

    def model_post_init(self, __context: Any) -> None:
        """Derive confidence level from score after init."""
        score = self.overall_confidence
        if score < 0.2:
            self.confidence_level = ConfidenceLevel.VERY_LOW
        elif score < 0.4:
            self.confidence_level = ConfidenceLevel.LOW
        elif score < 0.6:
            self.confidence_level = ConfidenceLevel.MEDIUM
        elif score < 0.8:
            self.confidence_level = ConfidenceLevel.HIGH
        else:
            self.confidence_level = ConfidenceLevel.VERY_HIGH


